--[[ opts.lua ]]
local opt = vim.opt

opt.number = true -- line numbers
opt.autoindent = true -- auto indent
opt.tabstop = 4 -- number of spaces to insert for a tab
opt.shiftwidth = 4 -- shift does 4 tabs
opt.softtabstop = 4 -- how many spaces cursor moves when you press tab
opt.clipboard = 'unnamedplus' -- able to copy + paste from vim to everything
